namespace G3.Maui.Core.Enums;

/// <summary>
/// Keyboard types for text input fields.
/// </summary>
public enum KeyboardType
{
    /// <summary>Default keyboard with no special capitalization.</summary>
    Default,

    /// <summary>URL keyboard optimized for entering web addresses.</summary>
    Url,

    /// <summary>Capitalizes the first letter of each sentence.</summary>
    CapitalizeSentence,

    /// <summary>Capitalizes the first letter of each word.</summary>
    CapitalizeWord
}
